export function dayOfYear(dateIso) {
  const [year, month, day] = dateIso.split('-').map(Number);
  const date = new Date(year, month - 1, day);
  const start = new Date(year, 0, 0);
  return Math.floor((date - start) / 86400000);
}

export function quoteForDate(dateIso) {
  const [year] = dateIso.split('-').map(Number);
  const offset = ((year * 73) % QUOTE_POOL.length + QUOTE_POOL.length) % QUOTE_POOL.length;
  const index = (offset + dayOfYear(dateIso) - 1) % QUOTE_POOL.length;
  return QUOTE_POOL[index];
}
